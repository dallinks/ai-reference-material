// One-shot generator: converts the legacy "AI Implementation Reference" course
// (the root app's data) into a Crucible course. It reads the old module data +
// per-module assessments, merges hand-authored review cards + diagrams, and
// emits a SELF-CONTAINED src/data/courses/ai-implementation.js (all data is
// inlined as JSON, so the file has no runtime dependency on the old app and
// survives the old app being archived).
//
// Run from the crucible/ dir:  node scripts/convert-ai-reference.mjs
//
// Block mapping: text/code/decision/checklist are field-identical to Crucible's
// renderer and pass through untouched. Only `diagram` differs in shape — we swap
// in the hand-authored Crucible `graph` block (by lesson id) or fall back to a
// faithful text rendering. Gates come from the existing assessments: each quiz
// question -> an `mcq`, and the hands-on exercise -> one AI-graded `open`.

import { COURSE } from "../../src/data/index.js";
import { ASSESSMENTS } from "../../src/data/assessments.js";
import { CARDS } from "./ai-reference.cards.js";
import { DIAGRAMS } from "./ai-reference.diagrams.js";
import { validateCourse } from "../src/lib/schema.js";
import { writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, "..", "src", "data", "courses", "ai-implementation.js");

const estMinutes = (d) => {
  const m = String(d || "").match(/\d+/);
  return m ? Number(m[0]) : 10;
};

// Faithful text fallback for any diagram not hand-authored into DIAGRAMS.
function diagramToText(b) {
  const sep = b.variant === "stack" ? "\n↓\n" : " → ";
  const chain = (b.nodes || [])
    .map((n) => (n.detail ? `**${n.label}** — ${n.detail}` : `**${n.label}**`))
    .join(sep);
  const cap = b.caption ? `\n\n*${b.caption}*` : "";
  return { type: "text", heading: b.heading, body: `${chain}${cap}` };
}

function mapBlocks(lesson) {
  return lesson.content.map((b) => {
    if (b.type !== "diagram") return b;
    return DIAGRAMS[b.heading] || diagramToText(b);
  });
}

function reviewItems(lessonId) {
  return (CARDS[lessonId] || []).map((c, i) => ({
    id: `${lessonId}-i${i + 1}`,
    front: c.front,
    back: c.back,
  }));
}

function masteryCheck(unitId, assessment) {
  const questions = [];
  (assessment?.quiz || []).forEach((q, i) => {
    questions.push({
      id: `${unitId}q${i + 1}`,
      type: "mcq",
      prompt: q.q,
      options: q.options,
      answer: q.answer,
      explanation: q.why,
    });
  });
  const ex = assessment?.exercise;
  if (ex) {
    questions.push({
      id: `${unitId}-ex`,
      type: "open",
      prompt: `${ex.scenario}\n\n**Task.** ${ex.task}`,
      rubric: ex.criteria,
      solution: ex.solution,
    });
  }
  return { id: `${unitId}-check`, questions };
}

const units = COURSE.modules.map((mod) => ({
  id: mod.id,
  title: mod.title,
  summary: mod.desc,
  masteryThreshold: 0.85,
  lessons: mod.lessons.map((l) => ({
    id: l.id,
    title: l.title,
    estMinutes: estMinutes(l.duration),
    content: mapBlocks(l),
    reviewItems: reviewItems(l.id),
  })),
  masteryCheck: masteryCheck(mod.id, ASSESSMENTS[mod.id]),
}));

const course = {
  id: "ai-implementation",
  title: "AI Implementation Reference",
  subject: "Applied Artificial Intelligence",
  difficulty: "Professional / Applied",
  description:
    "Build, deploy, and operate real AI systems — from how LLMs actually work through prompting, RAG, agents, the enterprise implementation patterns, and production operations. Practical and source-grounded, with code in C#/Semantic Kernel and Python/LangChain. The gates demand applied judgment, not recall.",
  grader:
    "You are a pragmatic principal AI engineer grading applied implementation decisions. Reward correct, specific, trade-off-aware reasoning; penalize buzzwords, hand-waving, and choices that ignore cost, latency, data, or failure modes.",
  sources: [
    "Adapted from the in-house 'AI Implementation Reference' course (9 modules, 39 lessons).",
  ],
  units,
};

const { ok, errors, warnings } = validateCourse(course);

const body = `// AUTO-GENERATED by scripts/convert-ai-reference.mjs from the legacy "AI
// Implementation Reference" course. Self-contained — all data is inlined, with
// no dependency on the original app. To regenerate, see the convert script and
// its sidecars (ai-reference.cards.js / ai-reference.diagrams.js).

export const aiImplementation = ${JSON.stringify(course, null, 2)};
`;

await writeFile(OUT, body, "utf8");

const lessonCount = units.reduce((s, u) => s + u.lessons.length, 0);
const cardCount = units.reduce(
  (s, u) => s + u.lessons.reduce((t, l) => t + l.reviewItems.length, 0),
  0
);
const qCount = units.reduce((s, u) => s + u.masteryCheck.questions.length, 0);
console.log(`Wrote ${OUT}`);
console.log(`Units ${units.length} · lessons ${lessonCount} · review cards ${cardCount} · gate questions ${qCount}`);
console.log(`Validation: ${ok ? "OK" : "FAILED"} — ${errors.length} errors, ${warnings.length} warnings`);
errors.forEach((e) => console.error("  ✗ " + e));
warnings.slice(0, 30).forEach((w) => console.warn("  ⚠ " + w));
if (warnings.length > 30) console.warn(`  … and ${warnings.length - 30} more warnings`);
if (!ok) process.exit(1);
